  Strom_Kalibrieren(&hadc1, &phasen);

  char init_msg[80];
  int init_len = sprintf(init_msg, "Offset: U=%u V=%u W=%u\r\n",
                         phasen.offset_U, phasen.offset_V, phasen.offset_W);
  HAL_UART_Transmit(&huart3, (uint8_t*)init_msg, init_len, 100);

